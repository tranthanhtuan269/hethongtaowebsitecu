function vnencode($s) {
	global $alphain, $alphaout;
	$s = str_replace(" – ","_",$s);
	$s = str_replace(" - ","_",$s);
	$s = str_replace($alphain, $alphaout, $s);
	$s = strtolower($s);
	$s = str_replace(array(" ","%",",","?",">","<","\\","/","^","@","!","#","'","\"","$","&","*","\(","\)","-","+",":","–",'“','”'),array("_","",".","","","","","_","","","","","","","","","","","","","","","","",""),$s);
	return $s;
}

